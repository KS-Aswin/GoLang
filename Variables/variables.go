package main

import "fmt"

func main() {
	var a = "initial"
	fmt.Println(a)

	var b, c int = 1, 2
	fmt.Println(b, c)

	var d = true
	fmt.Println(d)

	var e int
	fmt.Println(e)

	var f string
	fmt.Println(f)

	g := "String"
	fmt.Println(g)

	h := 12
	fmt.Println(h)

	var i, j = "Hello", "World"
	fmt.Println(i)
	fmt.Println(j)
	fmt.Println(i + j)

	var k, l = 1,2
	fmt.Println(k)
	fmt.Println(l)
	fmt.Println(k + l)

}
